AResBon::AResBon(AResManager* pResMan)
: base_class(pResMan)
{
}

AResBon::~AResBon(void)
{
}

bool AResBon::Init(const char* szPath)
{
	if (!base_class::Init(szPath))
		return false;

	return true;
}

void AResBon::Release()
{
	base_class::Release();
	m_aRefRes.clear();
}

//	Files that this resource ref to
int AResBon::GetRefResNum() const
{
	return static_cast<int>(m_aRefRes.size());
}

AResPtr AResBon::GetRefRes(int nIdx) const
{
	return m_aRefRes[nIdx];
}