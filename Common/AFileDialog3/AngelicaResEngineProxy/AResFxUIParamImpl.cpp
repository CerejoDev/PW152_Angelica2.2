AResFxUIParamImpl::AResFxUIParamImpl(void)
: m_bIsTex(false)
{
}

AResFxUIParamImpl::~AResFxUIParamImpl(void)
{
}

bool AResFxUIParamImpl::Init(A3DFXUIParam* pParam)
{
	m_bIsTex = (pParam->IsTexture() || pParam->IsAlphaTexture());
	
	if (m_bIsTex)
	{
		char szPath[MAX_PATH];
		pParam->GetValue(szPath, pParam->GetBytes());
		m_strTexPath = szPath;
	}

	return true;
}

bool AResFxUIParamImpl::IsParamTexture() const
{
	return m_bIsTex;
}

const char* AResFxUIParamImpl::GetTexPath() const
{
	return m_strTexPath;
}
